using SimpleIdentityServer.Configuration.Client.Factory;
using System;
using System.Net.Http;
using System.Threading.Tasks;

namespace SimpleIdentityServer.Configuration.Client.AuthProvider
{
    public interface IEnableAuthProviderOperation
    {
        Task<bool> ExecuteAsync(
            string name,
            string authenticationProviderUrl,
            string authorizationHeaderValue);
    }

    internal class EnableAuthProviderOperation : IEnableAuthProviderOperation
    {
        private readonly IHttpClientFactory _httpClientFactory;

        #region Constructor

        public EnableAuthProviderOperation(IHttpClientFactory httpClientFactory)
        {
            _httpClientFactory = httpClientFactory;
        }

        #endregion

        #region Public methods

        public async Task<bool> ExecuteAsync(
            string name,
            string authenticationProviderUrl,
            string authorizationHeaderValue)
        {
            if (string.IsNullOrWhiteSpace(name))
            {
                throw new ArgumentNullException(nameof(name));
            }

            if (string.IsNullOrWhiteSpace(authenticationProviderUrl))
            {
                throw new ArgumentNullException(nameof(authenticationProviderUrl));
            }

            if (string.IsNullOrWhiteSpace(authorizationHeaderValue))
            {
                throw new ArgumentNullException(nameof(authorizationHeaderValue));
            }

            Uri uri = null;
            if (!Uri.TryCreate(authenticationProviderUrl + "/" + name + "/enable", UriKind.Absolute, out uri))
            {
                throw new ArgumentException("the uri is not correct");
            }

            var httpClient = _httpClientFactory.GetHttpClient();
            var request = new HttpRequestMessage
            {
                Method = HttpMethod.Get,
                RequestUri = uri
            };
            request.Headers.Add("Authorization", "Bearer " + authorizationHeaderValue);
            var httpResult = await httpClient.SendAsync(request).ConfigureAwait(false);
            return httpResult.IsSuccessStatusCode;
        }

        #endregion
    }
}
